/**
 * 台灣電話號碼（手機 or 市話）的正則表達式
 *
 * 手機：
 *   09xx-xxx-xxx
 *   09xxxxxxxx
 *
 * 市話：
 *   (02)12345678
 *   02-12345678
 *   07-3456789
 *   0212345678
 *
 * 規則：
 * - 手機必須是 09 開頭 + 8 碼，共 10 碼
 * - 市話：區碼 (2~3 碼) + 號碼 (至少 7 碼)，總長 ≥ 10
 */

// 市話或手機
export const TW_PHONE_REGEX = /^(?:09\d{8}|09\d{2}-\d{3}-\d{3}|(?:\(0[2-8]\)\d{8}|0[2-8]-?\d{8}|\(0[2-8]\d\)\d{7}|0[2-8]\d-?\d{7}|\(0[2-8]\d{2}\)\d{6}|0[2-8]\d{2}-?\d{6}))$/

// 市話
export const TW_LANDLINE_REGEX = /^(?:(?:\(0[2-8]\)\d{8}|0[2-8]-?\d{8})|(?:\(0[2-8]\d\)\d{7}|0[2-8]\d-?\d{7})|(?:\(0[2-8]\d{2}\)\d{6}|0[2-8]\d{2}-?\d{6}))$/

// 手機
export const TW_MOBILE_REGEX = /^(?:09\d{8}|09\d{2}-\d{3}-\d{3})$/
